    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String phone = jTextField5.getText();
        if(phone.length() == 0){
            checkEmpty.setText("Enter the Phone number of seller");
        }
        else if(phone.length() != 11){
            checkEmpty.setText("Data not found");
        }else{
            // Check all the data that we need to fill up the form

            try{

                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/farmersdiary", "root", "");
                String sql = "select * from farmers where phone="+phone;
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                int i =0;
                while(rs.next()){
                    String fname = rs.getString("fname");
                    String lname = rs.getString("lname");
                    String customerName = fname+" "+lname;
                    String addres = rs.getString("Address");
                    String accNo = rs.getString("phone");
                    String gen = rs.getString("Gender");
                    //System.out.println(accNo);
                    name.setText(customerName.toUpperCase());
                    jTextField2.setText(accNo);
                    address.setText(addres);
                    gender.setSelectedItem(gen);

                    i++;
                }

            }catch(Exception e){
                System.out.println("Execption: "+e);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

